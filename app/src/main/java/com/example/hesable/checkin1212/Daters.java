package com.example.hesable.checkin1212;

public class Daters {
    String Name;
    String Phone;

    public Daters() {
    }

    public Daters(String name, String phone) {
        Name = name;
        Phone = phone;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getPhone() {
        return Phone;
    }

    public void setPhone(String phone) {
        Phone = phone;
    }
}
